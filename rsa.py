import math

p = 997
q = 883
e = 13  # 3120과 서로소인 11로 수정

n = q * p 
phi = (p - 1) * (q - 1)

# 이제 e=11이므로 정상적인 개인키 d = 1136을 정확히 찾아냅니다.
d = 1
while True:
    if (e * d) % phi == 1:
        break
    d += 1

# ===== 왜 이 숫자들(p=997, q=883, e=13)을 사용하는가? =====
#
# 1) p=997, q=883 → 둘 다 소수(Prime Number)여야 합니다.
#    RSA의 보안성은 두 소수의 곱 n을 소인수분해하기 어렵다는
#    수학적 난제에 기반합니다. 소수가 아니면 알고리즘이 성립하지 않습니다.
#
# 2) e=13 → 공개 지수 e는 φ(n)과 서로소(Coprime)여야 합니다.
#    φ(n) = (p-1)*(q-1) = 996*882 = 878,472
#    gcd(13, 878472) = 1 이므로 서로소 조건을 만족합니다.
#    만약 서로소가 아니면 개인키 d를 구할 수 없어 복호화가 불가능합니다.
#
# 3) 위 조건이 모두 만족되어야 개인키 d가 존재하고,
#    암호화(c = m^e mod n)와 복호화(m = c^d mod n)가 정상 동작합니다.
# ============================================================

message = input("암호화할 메시지를 입력하세요: ")

# 파이썬 거듭제곱 연산 정밀도 유지를 위해 pow 사용
m = []
h = []
result = ""
for char in message:
    m.append(pow(ord(char), e, n))
for i in m:
    h.append(pow(i, d, n))
for i in h:
    result += chr(i)
print(f"구해진 개인키 d: {d}")
print(f"암호화 결과 m: {m}")  # 이제 3000이 아니라 정상적인 암호문 '2587'이 뜹니다!
print(f"복호화 결과: {result}")